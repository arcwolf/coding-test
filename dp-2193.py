'''
[BOJ 2193] 이친수

문제
0과 1로만 이루어진 수를 이진수라 한다.
이 중 특수한 성질을 갖는 이친수(pinary number)는 다음과 같다.
1. 이친수는 0으로 시작하지 않는다.
2. 이친수에서는 1이 두 번 연속으로 나타나지 않는다. (11 불가)

N자리 이친수의 개수를 구하라.

입력
- N (1 ≤ N ≤ 90)

출력
- N자리 이친수의 개수

핵심 (DP 점화식)
dp[i][0] = i번째 자리가 0인 이친수 개수
dp[i][1] = i번째 자리가 1인 이친수 개수
dp[i][0] = dp[i-1][0] + dp[i-1][1]  # 앞자리 0,1 모두 가능
dp[i][1] = dp[i-1][0]                # 앞자리가 0일 때만 가능
→ 피보나치 수열과 동일한 구조
'''

N = int(input())

# dp[i][0]: i자리에서 마지막 숫자가 0인 이친수 개수
# dp[i][1]: i자리에서 마지막 숫자가 1인 이친수 개수
dp = [[0] * 2 for _ in range(N + 1)]

# 1자리: 이친수는 0으로 시작 불가 → 1만 가능
dp[1][1] = 1

for i in range(2, N + 1):
    dp[i][0] = dp[i-1][0] + dp[i-1][1]  # 앞자리가 0 또는 1일 때 0 추가 가능
    dp[i][1] = dp[i-1][0]               # 앞자리가 0일 때만 1 추가 가능 (11 방지)

print(dp[N][0] + dp[N][1])

